package weblerVizsga;

public class Kutyak {

	private Integer azonosito;
	private String becenev;
	private String fajta;
	private Byte kor;
	private String neme;
	private Byte ivartalanitott;

	public Integer getAzonosito() {
		return azonosito;
	}
	public void setAzonosito(Integer azonosito) {
		this.azonosito = azonosito;
	}
	public String getBecenev() {
		return becenev;
	}
	public void setBecenev(String becenev) {
		this.becenev = becenev;
	}
	public String getFajta() {
		return fajta;
	}
	public void setFajta(String fajta) {
		this.fajta = fajta;
	}
	public Byte getKor() {
		return kor;
	}
	public void setKor(Byte kor) {
		this.kor = kor;
	}
	public String getNeme() {
		return neme;
	}
	public void setNeme(String neme) {
		this.neme = neme;
	}
	public Byte getIvartalanitott() {
		return ivartalanitott;
	}
	public void setIvartalanitott(Byte ivartalanitott) {
		this.ivartalanitott = ivartalanitott;
	}

}
