package tripWageCalculator;

public class Auto {
	
	private String plateNum;
	private int consumption;
	private String fuelType;
	private int portablePeopleCount;
	private int engineVolumeCapacity;
	private int productionYear;
	
	public String getPlateNum() {
		return plateNum;
	}
	public void setPlateNum(String plateNum) {
		this.plateNum = plateNum;
	}
	public int getConsumption() {
		return consumption;
	}
	public void setConsumption(int consumption) {
		this.consumption = consumption;
	}
	public String getFuelType() {
		return fuelType;
	}
	public void setFuelType(String fuelType) {
		this.fuelType = fuelType;
	}
	public int getPortablePeopleCount() {
		return portablePeopleCount;
	}
	public void setPortablePeopleCount(int portablePeopleCount) {
		this.portablePeopleCount = portablePeopleCount;
	}
	public int getEngineVolumeCapacity() {
		return engineVolumeCapacity;
	}
	public void setEngineVolumeCapacity(int engineVolumeCapacity) {
		this.engineVolumeCapacity = engineVolumeCapacity;
	}
	public int getProductionYear() {
		return productionYear;
	}
	public void setProductionYear(int productionYear) {
		this.productionYear = productionYear;
	}
	
	

}
