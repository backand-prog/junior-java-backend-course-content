package szineszek;

import java.util.Date;

public class SzineszTarolo {

	private String nev;
	private Integer nem;
	private Date szulDatum;
	private String szulVaros;
	private String szulOrszag;
	private Short filmSzam;
	
	public String getNev() {
		return nev;
	}
	public void setNev(String nev) {
		this.nev = nev;
	}
	public Integer getNem() {
		return nem;
	}
	public void setNem(Integer nem) {
		this.nem = nem;
	}
	public Date getSzulDatum() {
		return szulDatum;
	}
	public void setSzulDatum(Date szulDatum) {
		this.szulDatum = szulDatum;
	}
	public String getSzulVaros() {
		return szulVaros;
	}
	public void setSzulVaros(String szulVaros) {
		this.szulVaros = szulVaros;
	}
	public String getSzulOrszag() {
		return szulOrszag;
	}
	public void setSzulOrszag(String szulOrszag) {
		this.szulOrszag = szulOrszag;
	}
	public Short getFilmSzam() {
		return filmSzam;
	}
	public void setFilmSzam(Short filmSzam) {
		this.filmSzam = filmSzam;
	}
	


	

}
