package ingatlan;

public class IngatlanTarolo {

	private String azonosíto;
	private String kerulet;
	private Short alapterulet;
	private Byte szobakSzama;
	private String ertekesitoNeve;
	private Boolean eladva;
	
	
	public String getAzonosíto() {
		return azonosíto;	
	}
	public void setAzonosíto(String azonosíto) {
		this.azonosíto = azonosíto;
	}
	public String getKerulet() {
		return kerulet;
	}
	public void setKerulet(String kerulet) {
		this.kerulet = kerulet;
	}
	public Short getAlapterulet() {
		return alapterulet;
	}
	public void setAlapterulet(Short alapterulet) {
		this.alapterulet = alapterulet;
	}
	public Byte getSzobakSzama() {
		return szobakSzama;
	}
	public void setSzobakSzama(Byte szobakSzama) {
		this.szobakSzama = szobakSzama;
	}
	public String getErtekesitoNeve() {
		return ertekesitoNeve;
	}
	public void setErtekesitoNeve(String ertekesitoNeve) {
		this.ertekesitoNeve = ertekesitoNeve;
	}
	public Boolean getEladva() {
		return eladva;
	}
	public void setEladva(Boolean eladva) {
		this.eladva = eladva;
	}

}


