package kezisek;

public class JatekosTarolo {
	
	Byte azonosito;
	String nev;
	String poszt;
	Byte golokSzama;
	Boolean serult;
	Byte merkozesekSzama;
	
	
	public Byte getAzonosito() {
		return azonosito;
	}
	public void setAzonosito(Byte azonosito) {
		this.azonosito = azonosito;
	}
	public String getNev() {
		return nev;
	}
	public void setNev(String nev) {
		this.nev = nev;
	}
	public String getPoszt() {
		return poszt;
	}
	public void setPoszt(String poszt) {
		this.poszt = poszt;
	}
	public Byte getGolokSzama() {
		return golokSzama;
	}
	public void setGolokSzama(Byte golokSzama) {
		this.golokSzama = golokSzama;
	}
	public Boolean getSerult() {
		return serult;
	}
	public void setSerult(Boolean serul) {
		this.serult = serul;
	}
	public Byte getMerkozesekSzama() {
		return merkozesekSzama;
	}
	public void setMerkozesekSzama(Byte merkozesekSzama) {
		this.merkozesekSzama = merkozesekSzama;
	}
	
	

}
